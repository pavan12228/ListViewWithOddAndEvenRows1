package com.blogspot.hongthaiit.listview;

public class FriendInfor {

	private String name;
	private boolean gender;
	
	public FriendInfor(String name, boolean gender) {
		this.name = name;
		this.gender = gender;
	}
	
	public String getName() {
		return name;
	}
	public boolean isGender() {
		return gender;
	}
	
}
